# For Loop

"""
    for kondisi:
        aksi
"""

angkaangka = [0,2,4,6,8] # ini adalah list
print(angkaangka)

for i in angkaangka:
    print(f"i sekarang -> {i}")

print("Akhir dari program 1\n")

angka = range(5) # ini adalah range

for i in angka:
    print(f"i sekarang -> {i}")

print("Akhir dari program 2\n")

angka = range(1,10) # ini adalah range

for i in angka:
    print(f"i sekarang -> {i}")

print("Akhir dari program 3\n")

